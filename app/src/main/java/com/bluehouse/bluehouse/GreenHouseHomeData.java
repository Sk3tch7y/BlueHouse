package com.bluehouse.bluehouse;

public class GreenHouseHomeData {

    private String ghName, ghTemp, ghHumidity, ghLight, ghOther;


    public void setGhName(String ghName) {
        this.ghName = ghName;
    }

    public void setGhTemp(String ghTemp) {
        this.ghTemp = ghTemp;
    }

    public void setGhHumidity(String ghHumidity) {
        this.ghHumidity = ghHumidity;
    }

    public void setGhLight(String ghLight) {
        this.ghLight = ghLight;
    }

    public void setGhOther(String ghOther) {
        this.ghOther = ghOther;
    }

    public String getGhName() {
        return ghName;
    }

    public String getGhTemp() {
        return ghTemp;
    }

    public String getGhHumidity() {
        return ghHumidity;
    }

    public String getGhLight() {
        return ghLight;
    }

    public String getGhOther() {
        return ghOther;
    }
}
